import UIKit

extension UIColor {
    
    static var appBlue: UIColor {
        return UIColor(named: "appBlue") ?? .clear
    }
    
    static var appGray: UIColor {
        return UIColor(named: "appGray") ?? .clear
    }
    
    static var appLightGray: UIColor {
        return UIColor(named: "appLightGray") ?? .clear
    }
    
}
